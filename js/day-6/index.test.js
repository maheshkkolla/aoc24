import {convertToMatrix} from "./index.js";

describe("Day 6", () => {
    describe("convertToMatrix", () => {
        test("should conver the given input to a matrix", () => {
           var input = "....#.....\n" + ".........#\n" + "..........\n" + "..#.......\n" + ".......#..\n" + "..........\n" + ".#..^.....\n" + "........#.\n" + "#.........\n" + "......#...";

           let actual = convertToMatrix(input);
           expect(actual).toEqual([
               [".", ".", ".", ".", "#", ".", ".", ".", ".", "."],
               [".", ".", ".", ".", ".", ".", ".", ".", ".", "#"],
               [".", ".", ".", ".", ".", ".", ".", ".", ".", "."],
               [".", ".", "#", ".", ".", ".", ".", ".", ".", "."],
               [".", ".", ".", ".", ".", ".", ".", "#", ".", "."],
               [".", ".", ".", ".", ".", ".", ".", ".", ".", "."],
               [".", "#", ".", ".", "^", ".", ".", ".", ".", "."],
               [".", ".", ".", ".", ".", ".", ".", ".", "#", "."],
               ["#", ".", ".", ".", ".", ".", ".", ".", ".", "."],
               [".", ".", ".", ".", ".", ".", "#", ".", ".", "."],
           ]);
        });
    });
});